public class Related {

	private int nid;
	private int vid;
	private int related_nid;
	
	public int getNid() {
		return nid;
	}
	public void setNid(int nid) {
		this.nid = nid;
	}
	public int getVid() {
		return vid;
	}
	public void setVid(int vid) {
		this.vid = vid;
	}
	public int getRelated_nid() {
		return related_nid;
	}
	public void setRelated_nid(int related_nid) {
		this.related_nid = related_nid;
	}
	
	
}
