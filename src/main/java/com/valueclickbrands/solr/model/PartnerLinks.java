public class PartnerLinks {

	private String partnerlinks_title;
	private String partnerlinks_url;
	private long nid;
	private long vid;
	
	public String getPartnerlinks_title() {
		return partnerlinks_title;
	}
	public void setPartnerlinks_title(String partnerlinks_title) {
		this.partnerlinks_title = partnerlinks_title;
	}
	public String getPartnerlinks_url() {
		return partnerlinks_url;
	}
	public void setPartnerlinks_url(String partnerlinks_url) {
		this.partnerlinks_url = partnerlinks_url;
	}
	public long getNid() {
		return nid;
	}
	public void setNid(long nid) {
		this.nid = nid;
	}
	public long getVid() {
		return vid;
	}
	public void setVid(long vid) {
		this.vid = vid;
	}
	
	
}
