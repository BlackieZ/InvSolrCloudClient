public class SynDate {

	private int nid;
	private int vid;
	private long synDate;
	public int getNid() {
		return nid;
	}
	public void setNid(int nid) {
		this.nid = nid;
	}
	public int getVid() {
		return vid;
	}
	public void setVid(int vid) {
		this.vid = vid;
	}
	public long getSynDate() {
		return synDate;
	}
	public void setSynDate(long synDate) {
		this.synDate = synDate;
	}
	
	
}
